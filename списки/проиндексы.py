a = [5, 4, 50, 100, 2, 6, 17, 0, 59, 4]
f = 0
# 1. Найти сумму элементов с четными индексами
for i in range(len(a)-1):
    if i % 2 == 0:
       f += a[i]
print(f)

# 2. Найти максимальный элемент с четным индексом
m = a[0]
for i in range(len(a)-1):
    if i % 2 == 0:
        if a[i] > m:
            m = a[i]
print(m)

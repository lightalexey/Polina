for i in range(10):  # 0..9
    print(i, end=' ')
print()
for i in range(0, 10):  # 0..9
    print(i, end=' ')
print()
for i in range(1, 10 + 1):  # 1..10
    print(i, end=' ')
print()
for i in range(1, 10 + 1, 2):  # 1 3 5 7 9
    print(i, end=' ')
print()
for i in range(100, 1, -3):
    print(i, end=' ')